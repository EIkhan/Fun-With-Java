public class Comparison {
    public static void main(String[] args) {
        int value1 = 12;
        int value2 = 25;
        if (value1 == value2)
            System.out.println("value1==value2");
        if (value1 != value2)
            System.out.println("value1!=value2");
        if (value1 > value2)
            System.out.println("value1>value2");
        if(value1<value2)
            System.out.println("value1<value2");
        if(value1<=value2)
            System.out.println("value1<=value2");

    }
}
